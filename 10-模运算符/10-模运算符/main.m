#import <Foundation/Foundation.h>

int main(int argc, const char * argv[]) {
    @autoreleasepool {
        int a= 25,b = 30,c = 40,d = 50;
        NSLog(@"a %% b = %i",a % b);
        NSLog(@"b %% c = %i",b % c);
        NSLog(@"c %% d = %i",c % d);
        
    }
    return 0;
}
